#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putstr(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, "\n", 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str != 0)
	{
		ft_putstr(par[i].str);
		write(1, &par[i].size, 1);
		write(1, "\n", 1);
		ft_putstr(par[i].copy);
		i++;
	}
}
