int	ft_atoi(char *str)
{
	int	i;
	int	nbr;
	int	n;

	i = 0;
	nbr = 0;
	n = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n++;
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	if (n % 2 == 1)
		return (-nbr);
	return (nbr);
}
