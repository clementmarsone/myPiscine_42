#include "ft_list.h"

t_list	*ft_list_at(t_list *begin_list, unsigned int nbr)
{
	unsigned int	i;

	i = 0;
	while (begin_list)
	{
		if (i == nbr)
			return (begin_list);
		if (begin_list->next == 0)
			return (0);
		begin_list = begin_list->next;
		i++;
	}
	return (0);
}
